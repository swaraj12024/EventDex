package co.comorin.eventdex.utils;

public class UserDetails {
   private String firstName, lastName, email, orderItemName, orderIdemId, lastModifiesDate;

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getOrderItemName() {
        return orderItemName;
    }

    public void setOrderItemName(String orderItemName) {
        this.orderItemName = orderItemName;
    }

    public String getOrderIdemId() {
        return orderIdemId;
    }

    public void setOrderIdemId(String orderIdemId) {
        this.orderIdemId = orderIdemId;
    }

    public String getLastModifiesDate() {
        return lastModifiesDate;
    }

    public void setLastModifiesDate(String lastModifiesDate) {
        this.lastModifiesDate = lastModifiesDate;
    }
}
